import { mkdir, readFile, readdir, writeFile } from 'node:fs/promises';
import { extname, relative, resolve, sep } from 'node:path';

const argumentsByName = parseArguments(process.argv.slice(2));
const inputDirectory = requireArgument(argumentsByName, '--input');
const headerPath = requireArgument(argumentsByName, '--header');
const sourcePath = requireArgument(argumentsByName, '--source');
const files = await collectFiles(inputDirectory);

if (!files.some((file) => file.resourcePath === '/index.html')) {
  throw new Error(`Static frontend output at ${inputDirectory} does not contain index.html.`);
}

const resources = await Promise.all(
  files.map(async (file, index) => ({
    ...file,
    bytes: await readFile(file.absolutePath),
    identifier: `asset_${index}`,
    mimeType: getMimeType(file.resourcePath)
  }))
);

await mkdir(resolve(headerPath, '..'), { recursive: true });
await mkdir(resolve(sourcePath, '..'), { recursive: true });
await writeFile(headerPath, createHeader(), 'utf8');
await writeFile(sourcePath, createSource(resources), 'utf8');

function parseArguments(values) {
  const result = new Map();
  for (let index = 0; index < values.length; index += 2) {
    const key = values[index];
    const value = values[index + 1];
    if (key === undefined || value === undefined) {
      throw new Error('Arguments must be provided as --name value pairs.');
    }
    result.set(key, value);
  }
  return result;
}

function requireArgument(values, name) {
  const value = values.get(name);
  if (value === undefined || value.length === 0) {
    throw new Error(`Missing required argument ${name}.`);
  }
  return resolve(value);
}

async function collectFiles(rootDirectory) {
  const root = resolve(rootDirectory);
  const entries = [];

  async function visit(directory) {
    const children = await readdir(directory, { withFileTypes: true });
    children.sort((left, right) => left.name.localeCompare(right.name, 'en'));

    for (const child of children) {
      const absolutePath = resolve(directory, child.name);
      if (child.isDirectory()) {
        await visit(absolutePath);
      } else if (child.isFile()) {
        const resourcePath = `/${relative(root, absolutePath).split(sep).join('/')}`;
        entries.push({ absolutePath, resourcePath });
      }
    }
  }

  await visit(root);
  entries.sort((left, right) => left.resourcePath.localeCompare(right.resourcePath, 'en'));
  return entries;
}

function createHeader() {
  return `// Generated by runtime/tools/embed-frontend.mjs. Do not edit.\n#pragma once\n\n#include <cstddef>\n#include <optional>\n#include <string_view>\n\nnamespace easy_plugin::frontend_assets\n{\nstruct AssetView\n{\n    const std::byte* data;\n    std::size_t size;\n    std::string_view mimeType;\n};\n\n[[nodiscard]] std::optional<AssetView> find(std::string_view path) noexcept;\n}\n`;
}

function createSource(resources) {
  const byteArrays = resources
    .map(({ bytes, identifier }) => {
      const values = Array.from(bytes, (byte) => `std::byte{0x${byte.toString(16).padStart(2, '0')}}`);
      return `constexpr std::array ${identifier}{\n    ${values.join(', ')}\n};`;
    })
    .join('\n\n');

  const rows = resources
    .map(
      ({ identifier, mimeType, resourcePath }) =>
        `    ResourceEntry{ "${escapeCpp(resourcePath)}", ${identifier}.data(), ${identifier}.size(), "${escapeCpp(mimeType)}" }`
    )
    .join(',\n');

  return `// Generated by runtime/tools/embed-frontend.mjs. Do not edit.\n#include "FrontendAssets.h"\n\n#include <algorithm>\n#include <array>\n\nnamespace easy_plugin::frontend_assets\n{\nnamespace\n{\nstruct ResourceEntry\n{\n    std::string_view path;\n    const std::byte* data;\n    std::size_t size;\n    std::string_view mimeType;\n};\n\n${byteArrays}\n\nconstexpr std::array resources{\n${rows}\n};\n}\n\nstd::optional<AssetView> find(std::string_view path) noexcept\n{\n    if (path == "/")\n        path = "/index.html";\n\n    const auto queryPosition = path.find_first_of("?#");\n    if (queryPosition != std::string_view::npos)\n        path = path.substr(0, queryPosition);\n\n    const auto iterator = std::lower_bound(\n        resources.begin(),\n        resources.end(),\n        path,\n        [] (const ResourceEntry& entry, std::string_view key) { return entry.path < key; });\n\n    if (iterator == resources.end() || iterator->path != path)\n        return std::nullopt;\n\n    return AssetView{ iterator->data, iterator->size, iterator->mimeType };\n}\n}\n`;
}

function getMimeType(path) {
  const mimeTypes = new Map([
    ['.css', 'text/css'],
    ['.gif', 'image/gif'],
    ['.html', 'text/html'],
    ['.ico', 'image/x-icon'],
    ['.jpeg', 'image/jpeg'],
    ['.jpg', 'image/jpeg'],
    ['.js', 'text/javascript'],
    ['.json', 'application/json'],
    ['.map', 'application/json'],
    ['.png', 'image/png'],
    ['.svg', 'image/svg+xml'],
    ['.txt', 'text/plain'],
    ['.webp', 'image/webp'],
    ['.woff', 'font/woff'],
    ['.woff2', 'font/woff2']
  ]);

  return mimeTypes.get(extname(path).toLowerCase()) ?? 'application/octet-stream';
}

function escapeCpp(value) {
  return value.replaceAll('\\', '\\\\').replaceAll('"', '\\"');
}

